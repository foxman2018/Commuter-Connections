<script>

  $(document).ready(function() {

    var offset = 300;
    scrollDuration = 500;

    $(window).scroll(function() {
      if ($(this).scrollTop() > offset) {
        $('.top').fadeIn(500);
      } else {
        $('.top').fadeOut(500);
      }
    });

    $('.top').click(function(event) {
      event.preventDefault();
      $('html, body').animate({
        scrollTop: 0
      }, scrollDuration);
    })
  });

  $('.nav-tab-about').click(function(){
    var goPage = $(this).attr('href');
    pos = $(goPage).offset().top;
    $('html, body').animate({
      scrollTop : pos, left : 0
    }, scrollDuration);
  });

  $( ".sign-up" ).click(function() {
    $( ".sign-up-box" ).addClass( "reply-shown" );
    $( ".opacity-screen" ).addClass( "display" );
  });

  $( ".nav-slide-about" ).click(function() {
    $( ".navbar-about" ).addClass( "nav-shown" );
    $( ".opacity-screen" ).addClass( "display" );
  });

  $( ".opacity-screen, .nav-tabs-about" ).click(function() {
    $( ".navbar-about" ).removeClass( "nav-shown" );
    $( ".opacity-screen" ).removeClass( "display" );
  });

</script>


